import { MonoText } from '../components/StyledText';


class Row extends React.Component{

  _onClick () {
      this.props.onClick(this.props.data);
  }

  shouldComponentUpdate (nextProps, nextState) {
      return false;
  }

  render () {
      console.log("rendering row");
      return (
          <TouchableHighlight onPress={this._onClick.bind(this)} style={{ backgroundColor: 'white', }}
              underlayColor ='transparent'
              >
              <View style={{
                  flex : 1,
                    padding: 10, borderColor: '#ccc',
                  borderBottomWidth: 1,
              }}>
                  <View style={styles.row}>
                      <Image resizeMode='contain' style={styles.image} source={{ uri: this.props.data.info.logo }}/>
                      <View style={{ flexDirection:'column', flex:1,marginLeft : 10, }}>
                              <Text style = {{fontSize: 14,}}>{this.props.data.info.name}</Text>
                              <Text style = {{fontSize: 9, alignSelf:'flex-start',}}>{this.props.data.info.url}</Text>
                      </View>
                  </View>

              </View>
          </TouchableHighlight>
      );
  }
}


export default class HomeScreen extends React.Component {
  static navigationOptions = {
    header: null
    ,
  };


  constructor(props){
    super(props);
    this.state ={ isLoading: true}
  }

  _onClick(rowData) {
    this.props.navigation.push('VideoList',{data : rowData, });
   
   //RNYouTubePlayer.play("AIzaSyDnHq8g0MqzS4Wf4xRdrNfa5_YyIA5pT6k", "xfeys7Jfnx8");
 }

  componentDidMount(){
    return fetch('https://leapar.github.io/sites.json')
      .then((response) => response.json())
      .then((responseJson) => {

        console.log(responseJson);
        this.setState({
          isLoading: false,
          dataSource: responseJson,
        }, function(){

        });

      })
      .catch((error) =>{
        console.error(error);
      });
  }


  render(){

    if(this.state.isLoading){
      return(
        <View style={{flex: 1, padding: 20}}>
          <ActivityIndicator/>
        </View>
      )
    }

    return(
      <View style={{flex: 1, paddingTop:20}}>
        <FlatList
          data={this.state.dataSource}
          renderItem={({item}) => <Row data={item} onClick={this._onClick.bind(this)}></Row>}
          keyExtractor={(item, index) => index+""}
        />
      </View>
    );
  }

  render2() {
    return (
      <View style={styles.container}>
        <ScrollView style={styles.container} contentContainerStyle={styles.contentContainer}>
          <View style={styles.welcomeContainer}>
            <Image
              source={
                __DEV__
                  ? require('../assets/images/robot-dev.png')
                  : require('../assets/images/robot-prod.png')
              }
              style={styles.welcomeImage}
            />
          </View>

          <View style={styles.getStartedContainer}>
            {this._maybeRenderDevelopmentModeWarning()}

            <Text style={styles.getStartedText}>Get started by opening</Text>

            <View style={[styles.codeHighlightContainer, styles.homeScreenFilename]}>
              <MonoText style={styles.codeHighlightText}>screens/HomeScreen.js</MonoText>
            </View>

            <Text style={styles.getStartedText}>
              Change this text and your app will automatically reload噢噢噢999噢.
            </Text>
          </View>

          <View style={styles.helpContainer}>
            <TouchableOpacity onPress={this._handleHelpPress} style={styles.helpLink}>
              <Text style={styles.helpLinkText}>Help, it didn’t automatically reload!</Text>
            </TouchableOpacity>
          </View>
        </ScrollView>

        <View style={styles.tabBarInfoContainer}>
          <Text style={styles.tabBarInfoText}>This is a tab bar. You can edit it in:</Text>

          <View style={[styles.codeHighlightContainer, styles.navigationFilename]}>
            <MonoText style={styles.codeHighlightText}>navigation/MainTabNavigator.js</MonoText>
          </View>
        </View>
      </View>
    );
  }

  _maybeRenderDevelopmentModeWarning() {
    if (__DEV__) {
      const learnMoreButton = (
        <Text onPress={this._handleLearnMorePress} style={styles.helpLinkText}>
          Learn more
        </Text>
      );

      return (
        <Text style={styles.developmentModeText}>
          Development mode is enabled, your app will be slower but you can use useful development
          tools. {learnMoreButton}
        </Text>
      );
    } else {
      return (
        <Text style={styles.developmentModeText}>
          You are not in development mode, your app will run at full speed.
        </Text>
      );
    }
  }

  _handleLearnMorePress = () => {
    WebBrowser.openBrowserAsync('https://docs.expo.io/versions/latest/guides/development-mode');
  };

  _handleHelpPress = () => {
    WebBrowser.openBrowserAsync(
      'https://docs.expo.io/versions/latest/guides/up-and-running.html#can-t-see-your-changes'
    );
  };
}

